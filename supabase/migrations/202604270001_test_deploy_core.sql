-- Local Atlas core schema upgrade.
-- Project-owned tables use the required `mlp_` prefix.
-- Existing public.mylocalplace rows are preserved by renaming the table to
-- public.mlp_mylocalplace when the prefixed table does not already exist.

create extension if not exists pgcrypto;

do $$
begin
  if to_regclass('public.mlp_mylocalplace') is null
     and to_regclass('public.mylocalplace') is not null then
    alter table public.mylocalplace rename to mlp_mylocalplace;
  end if;

  if to_regclass('public.mlp_profiles') is null
     and to_regclass('public.profiles') is not null then
    alter table public.profiles rename to mlp_profiles;
  end if;

  if to_regclass('public.mlp_capsule_discoveries') is null
     and to_regclass('public.capsule_discoveries') is not null then
    alter table public.capsule_discoveries rename to mlp_capsule_discoveries;
  end if;

  if to_regclass('public.mlp_capsule_reports') is null
     and to_regclass('public.capsule_reports') is not null then
    alter table public.capsule_reports rename to mlp_capsule_reports;
  end if;
end;
$$;

create table if not exists public.mlp_profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nickname text not null default 'Explorer',
  avatar_url text,
  energy integer not null default 100 check (energy between 0 and 100),
  master_keys integer not null default 3 check (master_keys >= 0),
  is_pro boolean not null default false,
  subscription_end timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.mlp_mylocalplace (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  lat double precision not null,
  lng double precision not null,
  title text not null,
  hint text,
  difficulty smallint default 3,
  access_limit integer default 5,
  category text default '🧩 기타 아지트',
  user_id uuid,
  is_promoted boolean default false
);

alter table public.mlp_mylocalplace
  add column if not exists access_count integer not null default 0,
  add column if not exists unlock_radius_meters integer not null default 50,
  add column if not exists updated_at timestamptz not null default now();

update public.mlp_mylocalplace
   set access_count = coalesce(access_count, 0),
       unlock_radius_meters = coalesce(unlock_radius_meters, 50),
       updated_at = coalesce(updated_at, created_at, now()),
       hint = coalesce(hint, ''),
       difficulty = coalesce(difficulty, 3),
       access_limit = coalesce(access_limit, 5),
       category = coalesce(category, '🧩 기타 아지트'),
       is_promoted = coalesce(is_promoted, false);

create table if not exists public.mlp_capsule_discoveries (
  id uuid primary key default gen_random_uuid(),
  capsule_id bigint not null references public.mlp_mylocalplace(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  distance_meters numeric(10, 2) not null,
  created_at timestamptz not null default now(),
  unique (capsule_id, user_id)
);

create table if not exists public.mlp_capsule_reports (
  id uuid primary key default gen_random_uuid(),
  capsule_id bigint not null references public.mlp_mylocalplace(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  reason text not null,
  detail text,
  status text not null default 'open',
  created_at timestamptz not null default now()
);

