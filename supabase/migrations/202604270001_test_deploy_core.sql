create or replace function public.mlp_handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.mlp_profiles (id, nickname)
  values (
    new.id,
    coalesce(
      nullif(new.raw_user_meta_data ->> 'nickname', ''),
      split_part(new.email, '@', 1),
      'Explorer'
    )
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.mlp_handle_new_user();

create or replace function public.mlp_distance_meters(
  lat_a double precision,
  lng_a double precision,
  lat_b double precision,
  lng_b double precision
)
returns double precision
language sql
immutable
as $$
  select 6371000 * 2 * asin(
    sqrt(
      power(sin(radians((lat_b - lat_a) / 2)), 2) +
      cos(radians(lat_a)) *
      cos(radians(lat_b)) *
      power(sin(radians((lng_b - lng_a) / 2)), 2)
    )
  );
$$;

