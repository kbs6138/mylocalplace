create or replace function public.mlp_verify_and_unlock(
  u_lat double precision,
  u_lng double precision,
  target_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid := auth.uid();
  target public.mlp_mylocalplace%rowtype;
  distance double precision;
  already_unlocked boolean;
  unlock_rank integer;
begin
  if current_user_id is null then
    return jsonb_build_object('ok', false, 'code', 'AUTH_REQUIRED', 'message', '로그인이 필요합니다.');
  end if;

  select *
    into target
    from public.mlp_mylocalplace
   where id = target_id
     and (
       is_promoted = true
       or user_id = current_user_id
       or user_id is null
       or exists (
         select 1
           from public.mlp_capsule_discoveries
          where capsule_id = target_id
            and user_id = current_user_id
       )
     );

  if not found then
    return jsonb_build_object('ok', false, 'code', 'NOT_FOUND', 'message', '열람할 수 없는 아지트입니다.');
  end if;

  distance := public.mlp_distance_meters(u_lat, u_lng, target.lat, target.lng);

  if distance > coalesce(target.unlock_radius_meters, 50) then
    return jsonb_build_object(
      'ok', false,
      'code', 'TOO_FAR',
      'message', '아직 충분히 가까이 가지 않았습니다.',
      'distance_meters', round(distance::numeric, 2),
      'required_meters', coalesce(target.unlock_radius_meters, 50)
    );
  end if;

  select exists (
    select 1
      from public.mlp_capsule_discoveries
     where capsule_id = target.id
       and user_id = current_user_id
  ) into already_unlocked;

  if coalesce(target.access_count, 0) >= coalesce(target.access_limit, 5) and not already_unlocked then
    return jsonb_build_object(
      'ok', false,
      'code', 'ACCESS_LIMIT_REACHED',
      'message', '선착순 열람 가능 인원이 모두 찼습니다.'
    );
  end if;

  if not already_unlocked then
    insert into public.mlp_capsule_discoveries (capsule_id, user_id, distance_meters)
    values (target.id, current_user_id, round(distance::numeric, 2));

    update public.mlp_mylocalplace
       set access_count = coalesce(access_count, 0) + 1
     where id = target.id;
  end if;

  select count(*)
    into unlock_rank
    from public.mlp_capsule_discoveries
   where capsule_id = target.id
     and created_at <= (
       select created_at
         from public.mlp_capsule_discoveries
        where capsule_id = target.id
          and user_id = current_user_id
     );

  return jsonb_build_object(
    'ok', true,
    'code', 'UNLOCKED',
    'title', target.title,
    'hint', coalesce(target.hint, ''),
    'distance_meters', round(distance::numeric, 2),
    'rank', unlock_rank,
    'already_unlocked', already_unlocked
  );
end;
$$;

